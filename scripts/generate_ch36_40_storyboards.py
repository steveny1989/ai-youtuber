def main() -> int:
    import json
    from hybrid_storyboard_util import EXAMPLES

    ch40_path = EXAMPLES / "storyboard-daodejing-ch40-commentary.json"
    ch40_narrs = {
        s["id"]: s["narration"]
        for s in json.loads(ch40_path.read_text(encoding="utf-8"))["scenes"]
    }

    for ch, meta in CHAPTER_META.items():
        narrs = NARRATIONS.get(ch) or ch40_narrs
        meta["scenes"] = [(sid, narrs[sid]) for sid in SCENE_ORDER]
        if "观念黑盒起号" not in meta.get("bili_dynamic", ""):
            meta["bili_dynamic"] = (
                meta["bili_dynamic"].rstrip("。") + "。" + GROWTH_DYNAMIC_SUFFIX
            )

    reset_batch_image_tracking()
    write_chapters(
        CHAPTER_META,
        first_chapter=36,
        max_chapter=40,
        tts_provider="edge",
        read_rate="-15%",
        long_form=True,
        min_commentary_chars=1200,
    )

    for ch in range(36, 41):
        p = EXAMPLES / f"storyboard-daodejing-ch{ch:02d}-commentary.json"
        sb = json.loads(p.read_text(encoding="utf-8"))
        sb["tts"]["rate"] = "-10%"
        p.write_text(json.dumps(sb, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print("Set commentary TTS rate -10% for ch36–40")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
