def strip_boilerplate(text: str) -> str:
    out = text
    for phrase in STRIP_PHRASES:
        out = out.replace(phrase, "")
    out = re.sub(r"\s+", " ", out)
    out = re.sub(r" +([。！？，；])", r"\1", out)
    return out.strip()


def apply_scene_enrichments(
    scenes: list[tuple[str, str]], chapter: int
) -> list[tuple[str, str]]:
    enrich = SCENE_ENRICHMENTS.get(chapter, {})
    out: list[tuple[str, str]] = []
    for sid, narr in scenes:
        extra = enrich.get(sid, "")
        combined = clean_narration(strip_boilerplate(narr + extra), chapter)
        out.append((sid, combined))
    return out


def clean_narration(text: str, chapter: int | None = None) -> str:
    text = strip_boilerplate(text)
    text = text.replace("All in", "孤注一掷")
    text = text.replace(" skin", "皮肤")
    return text.strip()
